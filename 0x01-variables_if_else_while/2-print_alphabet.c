#include <stdio.h>


/**
 * main - initializer, single line
 * @: Description of parameter x
(* a blank line
 * Description: This function prints a to z in
 * lower case)?
(* section header: Section description)*
 * Return: Terminates the code
 */
int main(void)
{
	char ny = 'a';

	while (ny <= 'z')
	{
		putchar(ny);
		ny++;
	}
	return(0);
}
